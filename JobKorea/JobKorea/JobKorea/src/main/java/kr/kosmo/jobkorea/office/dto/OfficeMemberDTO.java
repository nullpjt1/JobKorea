package kr.kosmo.jobkorea.office.dto;

public class OfficeMemberDTO {
	
	 private String officeId;
	 private String password;
	 private String mgr_name; 
	 private String office_name;
	 private String email1;
	 private String email2;
	 private String email_select;
	 private int birth;
	 private int ph1;
	 private int ph2;
	 private int ph3;
	 private String company_info;
	 
	 
	public OfficeMemberDTO(String officeId, String password, String mgr_name, String office_name, String email1,
			String email2, String email_select, int birth, int ph1, int ph2, int ph3, String company_info) {
		super();
		this.officeId = officeId;
		this.password = password;
		this.mgr_name = mgr_name;
		this.office_name = office_name;
		this.email1 = email1;
		this.email2 = email2;
		this.email_select = email_select;
		this.birth = birth;
		this.ph1 = ph1;
		this.ph2 = ph2;
		this.ph3 = ph3;
		this.company_info = company_info;
	}
	
	
	
	public OfficeMemberDTO() {
		super();
	}
	
	
	public String getOfficeId() {
		return officeId;
	}
	public void setOfficeId(String officeId) {
		this.officeId = officeId;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getMgr_name() {
		return mgr_name;
	}
	public void setMgr_name(String mgr_name) {
		this.mgr_name = mgr_name;
	}
	public String getOffice_name() {
		return office_name;
	}
	public void setOffice_name(String office_name) {
		this.office_name = office_name;
	}
	public String getEmail1() {
		return email1;
	}
	public void setEmail1(String email1) {
		this.email1 = email1;
	}
	public String getEmail2() {
		return email2;
	}
	public void setEmail2(String email2) {
		this.email2 = email2;
	}
	public String getEmail_select() {
		return email_select;
	}
	public void setEmail_select(String email_select) {
		this.email_select = email_select;
	}
	public int getBirth() {
		return birth;
	}
	public void setBirth(int birth) {
		this.birth = birth;
	}
	public int getPh1() {
		return ph1;
	}
	public void setPh1(int ph1) {
		this.ph1 = ph1;
	}
	public int getPh2() {
		return ph2;
	}
	public void setPh2(int ph2) {
		this.ph2 = ph2;
	}
	public int getPh3() {
		return ph3;
	}
	public void setPh3(int ph3) {
		this.ph3 = ph3;
	}
	public String getCompany_info() {
		return company_info;
	}
	public void setCompany_info(String company_info) {
		this.company_info = company_info;
	}
	 
	 

}
