package kr.kosmo.jobkorea.freeboard.dto;

public class FreeboardDTO {
	
	private String wno; //글번호
	private String title;//제목
	private String note;  //내용
	private String upper_wno;//상위글 번호
	private String regId; //등록자
	private String reg_date; //등록일자
	
	
	public FreeboardDTO(String wno, String title, String note, String upper_wno, String regId, String reg_date) {
		super();
		this.wno = wno;
		this.title = title;
		this.note = note;
		this.upper_wno = upper_wno;
		this.regId = regId;
		this.reg_date = reg_date;
	}


	public String getWno() {
		return wno;
	}


	public void setWno(String wno) {
		this.wno = wno;
	}


	public String getTitle() {
		return title;
	}


	public void setTitle(String title) {
		this.title = title;
	}


	public String getNote() {
		return note;
	}


	public void setNote(String note) {
		this.note = note;
	}


	public String getUpper_wno() {
		return upper_wno;
	}


	public void setUpper_wno(String upper_wno) {
		this.upper_wno = upper_wno;
	}


	public String getRegId() {
		return regId;
	}


	public void setRegId(String regId) {
		this.regId = regId;
	}


	public String getReg_date() {
		return reg_date;
	}


	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	
	
	

}
